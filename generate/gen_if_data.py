# 14 種題型及其比例權重（對齊推論集分布：朗讀/列舉/數數/序列佔大宗）
QTYPE_WEIGHTS = {
    "朗讀":   8,   # 朗讀指定句子（推論集最核心）
    "列舉":   4,   # 說出 N 種某類事物
    "數數":   3,   # 從 M 數到 N
    "倒數":   3,   # 從 N 倒數到 M
    "序列":   3,   # 說出固定序列（星期/月份/四季等）
    "複述":   3,   # 把句子重複一遍（措辭不同於朗讀）
    "描述":   3,   # 場景/事物短篇描述
    "感受描述": 2,  # 描述某種感官或情緒感受
    "比較":   2,   # 比較兩樣事物哪個更...
    "舉例":   2,   # 舉一個某類事物的例子
    "說出喜好": 2,  # 說出最喜歡/不喜歡的事物
    "問答":   3,   # 回答知識型問題
    "反義詞":  1,
    "同義詞":  1,
}

QTYPES = list(QTYPE_WEIGHTS.keys())
QTYPE_WEIGHT_LIST = [QTYPE_WEIGHTS[k] for k in QTYPES]

