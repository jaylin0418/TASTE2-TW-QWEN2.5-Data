"""
Type 3: Instruction Following (IF) dialogue generation.
Generates multi-turn dialogues where 甲 gives 3-6 IF tasks and 乙 executes them.
14 task types aligned with spoken-language-model evaluation set.

Task content (instructions + answers) is generated by the main LLM at runtime
using few-shot examples — no large static lists required.

Usage:
    python generate/gen_if_data.py --config conf/if_data.yaml \
        --num-dialogues 1600 --output output/if_data/
"""
